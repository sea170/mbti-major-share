// Flatten all majors for search
export const ALL_MAJORS: string[] = MAJOR_DIRECTORY.flatMap((cat) => cat.majors);

// Search majors by keyword
export function searchMajors(keyword: string, limit: number = 12): string[] {
  if (!keyword.trim()) return [];
  const kw = keyword.toLowerCase();
  const results = ALL_MAJORS.filter((m) => m.toLowerCase().includes(kw));
  return results.slice(0, limit);
}

// Get category name for a major
export function getMajorCategory(major: string): string | null {
  for (const cat of MAJOR_DIRECTORY) {
    if (cat.majors.includes(major)) return cat.name;
  }
  return null;
}
